from pet import Pet

# Create a pet
my_pet = Pet("Buddy")

# Show initial status
my_pet.get_status()

# Feed the pet
my_pet.eat()

# Let the pet sleep
my_pet.sleep()

# Play with the pet
my_pet.play()

# Teach some tricks
my_pet.train("roll over")
my_pet.train("fetch")

# Show all learned tricks
my_pet.show_tricks()

# Final status check
my_pet.get_status()
